    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {                                      
        // TODO add your handling code here:
        Graphics g= canvas1.getGraphics();
        g.setColor(Color.black);
        
            if(x+50>=500&&y+50>=500)
            {
                jLabel1.setText("Can't Add More DOTS");
            }
            else if(x+50<500)
            {
                x=x+50;
                g.fillOval(x, y, 2, 2);
                jLabel1.setText("Adding DOT");
                
            }
            else if(x+50>=500)
            {
                x=50;
                y=y+50;
                g.fillOval(x, y, 2, 2);
                jLabel1.setText("Adding DOT");
            }
    }                                     

