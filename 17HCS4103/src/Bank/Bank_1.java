    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String s=jTextField2.getText();
        if(!s.equalsIgnoreCase(""))
        {
            long w= Long.valueOf(jTextField2.getText());
            double balance=b1.accInfo(w);
            Account a2=b1.getAccount(w);
            if(a2==null)
            {
                jTextArea2.setText("No Such Account Found !!");
            }
            else
                jTextArea2.setText("\nName : "+a2.name+"\nAccount no : "+w+"\nBalance : "+balance);
        }
        else
        {
            jTextField2.setForeground(Color.red);
            jTextField2.setText("<Enter Account no.>");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

