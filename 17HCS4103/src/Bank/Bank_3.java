    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // ADD ACCOUNT
        String name= jTextField1.getText();
        
        Double w= Double.valueOf(jTextField2.getText());
        
        if(jButton1.getText().equalsIgnoreCase("confirm"))
        {
            jTextArea3.setText("ACCOUNT CREATED!!\n\nName : "+_name+"\nAccount no. : "+b1.base+"\nDeposited : "+_w);
            b1.addAccount(_w, _name);
            jButton1.setText("Add Account");
            jTextArea3.setForeground(Color.magenta);
            jTextField1.setForeground(Color.BLUE);
            jTextField1.setText("<Create new account>");
        }
        else
        {
            jLabel1.setText(Double.toString(b1.base));
            if(!(name.equalsIgnoreCase("")||name.equalsIgnoreCase("<Create new account>")||name.equalsIgnoreCase("<Enter Valid Name>")))
            {
                jTextArea3.setForeground(Color.BLUE);
                jTextArea3.setText("Create Account\n\nName : "+name+"\nAccount no. : "+b1.base+"\nDeposited : "+w);
                jTextField2.setText("0.0");
                jTextField1.setText("");
                jButton1.setText("Confirm");
                _name=name;
                _w=w;
            }
            else
            {
                jTextField1.setForeground(Color.RED);
                jTextField1.setText("<Enter Valid Name>");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

